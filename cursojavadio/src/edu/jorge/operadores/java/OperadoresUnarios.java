package edu.jorge.operadores.java;

public class OperadoresUnarios {

	public static void main(String[] args) {
		
		int numero = 5;
		
		System.out.println(- numero);
		System.out.println(numero);
		
		numero = - numero;
		System.out.println(numero);
		
		numero = numero * -1; // PARA TORNAR O NUMERO POSITIVO NOVAMENTE
		System.out.println(numero);
		
	}

}
